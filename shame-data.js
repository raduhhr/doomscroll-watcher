function getSourceLabel(sourceUrl) {
  try {
    const hostname = new URL(sourceUrl).hostname.replace(/^www\./i, "");
    return SOURCE_LABELS[hostname] || hostname;
  } catch (_error) {
    return sourceUrl;
  }
}

function normalizeEntries(hostname, entries) {
  return entries.map((entry, index) => ({
    id: `${hostname.replace(/[^a-z0-9]+/gi, "-")}-${index + 1}`,
    text: entry.text,
    source: getSourceLabel(entry.source),
    sourceUrl: entry.source,
    scope: hostname,
  }));
}

